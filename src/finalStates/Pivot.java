package finalStates;

/**
 * @Author Sergey Pensov
 */
public class Pivot {
    public int x;
    public int y;

    public int getX() {
        return x;
    }

    public void setX(int x) {
        this.x = x;
    }

    public int getY() {
        return y;
    }

    public void setY(int y) {
        this.y = y;
    }

    public Pivot(int x, int y) {
        this.x = x;
        this.y = y;
    }
}
