public class FPoint {
    public float x;
    public float y;

    public FPoint(float x, float y) {
        this.x = x;
        this.y = y;
    }

    @Override
    public String toString() {
        return "FPoint{" +
                "x=" + x +
                ", y=" + y +
                '}';
    }
}
